import UIKit


extension UIColor {
    
    @nonobjc class var ssLightBlue: UIColor {
        return UIColor(red: 140.0 / 255, green: 189.0 / 255.0, blue: 241.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var ssDarkBlue: UIColor {
        return UIColor(red: 106.0 / 255, green: 155.0 / 255.0, blue: 236.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var ssShadow: UIColor {
        return UIColor(white: 0, alpha: 140.0 / 255.0)
    }
    
    @nonobjc class var ssYellow: UIColor {
        return UIColor(red: 248.0 / 255, green: 231.0 / 255.0, blue: 28.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var ssDarkPeriwinkle: UIColor {
        return UIColor(red: 128.0 / 255, green: 121.0 / 255.0, blue: 217.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var ssIris: UIColor {
        return UIColor(red: 84.0 / 255, green: 87.0 / 255.0, blue: 185.0 / 255.0, alpha: 1.0)
    }
}
